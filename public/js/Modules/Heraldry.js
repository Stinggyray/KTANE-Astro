$(function () {
    const grids = $(".grid-table");
    for (let rowNum = 0; rowNum < 4; rowNum++) {
        const row = $("<tr>").appendTo(grids);
        for (let colNum = 0; colNum < 4; colNum++) {
            const cell = $("<td>").appendTo(row);
            cell.append(DrawGridSvg(rowNum * 4 + colNum));
        }
    }
});

const colourDict = { "0": "FFFA", "1": "000" };

function DrawGridSvg(gridNum) {
    const svg = $('<svg viewbox="0 0 85 95" width="95%" style="display:block" xmlns="http://www.w3.org/2000/svg">');

    svg.append(MakeSvgElem("text", { x: 0, y: 10 }, `${gridNum + 1}.`));

    for (let col = 0; col < 8; col++) {
        for (let row = 0; row < 8; row++) {
            const curColour = gridLayout[gridNum][row * 8 + col]
            svg.append(MakeSvgElem("rect", { x: 10 * col, y: 15 + 10 * row, width: 10, height: 10, fill: `#${colourDict[curColour]}` }));
        }
    }

    return svg
}