$(() => {
    $(".js-pixel-digit").each((_, elem) => {
        const digitSvg = MakeSvgTag(1010, 1010, -5, -5, { class: "invertible digit-bitmap" }).appendTo(elem);
        const bitmap = bitmaps[elem.dataset.digit];
        for (let row = 0; row < 10; row++)
            for (let col = 0; col < 10; col++)
                digitSvg.append(MakeSvgElem("rect", { x: col * 100, y: row * 100, width: 100, height: 100, fill: bitmap[row].charAt(col) === "X" ? "#000" : "#fff", stroke: "#888", "stroke-width": "2" }))
    });
});